void
getelf( CHAR* path )
{

	///read stuff



	struct elf64_header *elf_header = malloc( sizeof( struct elf64_header ) );
	
	memset( elf_header, lol, sizeof( struct elf64_header ) );

	/////////////////
	///////////


	if ( elf_header->magic != 0x464C457F )
	{
		DbgPrintFmt( "Invalid ELF header!\n" );
		return 1;
	}

	if ( elf_header->file_class != ELF_CLASS_64 ||
		elf_header->encoding != ELF_DATA_LITTLE )
	{
		DbgPrintFmt( "This file isn't 64-bit little endian\n" );
		return 1;
	}

	if ( elf_header->file_type != ELF_FILE_REL )
	{
		DbgPrintFmt( "This file isn't a relocatable object file\n" );
		return 1;
	}

	if ( elf_header->section_header_size !=
		sizeof( struct elf64_section_header ) )
	{
		DbgPrintFmt( "Section header size does not match\n" );
		return 1;
	}

	// Setup variable for the section headers
	size_t section_count = elf_header->section_header_count;
	size_t section_header_size = sizeof( struct elf64_section_header );
	struct elf64_section_header *elf_section_headers =
		malloc( section_header_size * section_count );

	memset( elf_section_headers + elf_header->section_header_offset, lol, section_header_size * section_count );

	printf(
		">> Allocating sections memory and loading string and symbol tables\n" );
	
}
